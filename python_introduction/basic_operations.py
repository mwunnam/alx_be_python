number1 = 10
number2 = 5

# Addition
add_result = number1 + number2

# Subtraction 
sub_result = number1 - number2

# Multiplication 
mul_result = number1 * number2

# Outputs
print("Addition of {} and {} is {}".format(number1, number2, add_result))
print("Subtraction of {} and {} is {}".format(number1, number2, sub_result))
print("Multiplication of {} and {} is {}".format(number1, number2, mul_result))